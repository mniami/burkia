package pl.guideme.data.vo;

public class KeyValuePair {
    private String mKey;
    private String mValue;

    public String getKey() {
        return mKey;
    }

    public void setKey(String key) {
        mKey = key;
    }

    public String getValue() {
        return mValue;
    }

    public void setValue(String value) {
        mValue = value;
    }
}
